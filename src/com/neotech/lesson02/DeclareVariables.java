package com.neotech.lesson02;

public class DeclareVariables {
	
	public static void main(String [] args) {
		
		byte ugurcan = -115;
		System.out.println(ugurcan);

		long caglar = 8920018799L;
		System.out.println(caglar);

		short merih = 32000;
		System.out.println(merih);
		
		int zeki;
		zeki= 19077788;
		System.out.println(zeki);
		
		float umut= 3.5f;
		System.out.println(umut);
		
		double okay= 898098.111;
		System.out.println(okay);
		
		char ozan = 'K';
		System.out.println(ozan);
		
		boolean cengiz= true ;
		System.out.println(cengiz);
		
		
		
	}

}
